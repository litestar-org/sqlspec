def _sync_oracle_table_exists(driver: Any, owner: str, table_name: str) -> bool:
    result = driver.execute("SELECT 1 FROM ALL_TABLES WHERE OWNER = :1 AND TABLE_NAME = :2", (owner, table_name))
    return bool(result.data)


async def _async_oracle_table_exists(driver: Any, owner: str, table_name: str) -> bool:
    result = await driver.execute("SELECT 1 FROM ALL_TABLES WHERE OWNER = :1 AND TABLE_NAME = :2", (owner, table_name))
    return bool(result.data)


def test_oracledb_sync_migration_default_schema_applies_to_ddl(
    tmp_path: Path, oracle_23ai_service: OracleService
) -> None:
    """Oracle sync migrations run unqualified DDL in the configured default schema."""
    schema = _oracle_identifier("OSD")
    table_name = _oracle_identifier("TBL")
    version_table = _oracle_identifier("OV")
    migration_dir = tmp_path / "migrations"

    config = OracleSyncConfig(
        connection_config={
            "host": oracle_23ai_service.host,
            "port": oracle_23ai_service.port,
            "service_name": oracle_23ai_service.service_name,
            "user": oracle_23ai_service.user,
            "password": oracle_23ai_service.password,
        },
        migration_config={
            "script_location": str(migration_dir),
            "version_table_name": version_table,
            "default_schema": schema,
        },
    )
    commands = SyncMigrationCommands(config)

    try:
        _create_oracle_schema(oracle_23ai_service, schema)

        commands.init(str(migration_dir), package=True)
        _write_unqualified_table_migration(migration_dir, table_name)
        commands.upgrade()

        with config.provide_session() as driver:
            assert _sync_oracle_table_exists(driver, schema, table_name)
            assert _sync_oracle_table_exists(driver, schema, version_table)
    finally:
        _drop_oracle_schema(oracle_23ai_service, schema)
        if config.connection_instance:
            config.close_pool()


