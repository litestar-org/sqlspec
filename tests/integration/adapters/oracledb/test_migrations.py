def test_oracledb_migration_separable_tracker_and_default_schema(
    tmp_path: Path, oracle_23ai_service: OracleService
) -> None:
    """Oracle supports separate schemas for migrated DDL and the tracker table."""
    default_schema = _oracle_identifier("ODS")
    tracker_schema = _oracle_identifier("OTS")
    table_name = _oracle_identifier("TBL")
    version_table = _oracle_identifier("OV")
    migration_dir = tmp_path / "migrations"

    config = OracleSyncConfig(
        connection_config={
            "host": oracle_23ai_service.host,
            "port": oracle_23ai_service.port,
            "service_name": oracle_23ai_service.service_name,
            "user": oracle_23ai_service.user,
            "password": oracle_23ai_service.password,
        },
        migration_config={
            "script_location": str(migration_dir),
            "version_table_name": version_table,
            "default_schema": default_schema,
            "version_table_schema": tracker_schema,
        },
    )
    commands = SyncMigrationCommands(config)

    try:
        _create_oracle_schema(oracle_23ai_service, default_schema)
        _create_oracle_schema(oracle_23ai_service, tracker_schema)

        commands.init(str(migration_dir), package=True)
        _write_unqualified_table_migration(migration_dir, table_name)
        commands.upgrade()

        with config.provide_session() as driver:
            assert _sync_oracle_table_exists(driver, default_schema, table_name)
            assert _sync_oracle_table_exists(driver, tracker_schema, version_table)
            assert not _sync_oracle_table_exists(driver, default_schema, version_table)
    finally:
        _drop_oracle_schema(oracle_23ai_service, default_schema)
        _drop_oracle_schema(oracle_23ai_service, tracker_schema)
        if config.connection_instance:
            config.close_pool()


