def _oracle_identifier(prefix: str) -> str:
    """Return a generated Oracle-safe identifier within the 30 byte limit."""
    return f"{prefix}_{uuid4().hex[:8]}".upper()


def _write_unqualified_table_migration(migration_dir: Path, table_name: str) -> None:
    migration_content = f'''"""Create an unqualified Oracle table."""


def up():
    """Create an unqualified table."""
    return ["""
        CREATE TABLE {table_name} (
            id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name VARCHAR2(255) NOT NULL
        )
    """]


def down():
    """Drop the unqualified table."""
    return ["DROP TABLE {table_name}"]
'''
    (migration_dir / "0001_create_unqualified_table.py").write_text(migration_content)


def _oracle_admin_sync_config(oracle_service: OracleService) -> OracleSyncConfig:
    return OracleSyncConfig(
        connection_config={
            "host": oracle_service.host,
            "port": oracle_service.port,
            "service_name": oracle_service.service_name,
            "user": "system",
            "password": oracle_service.system_password,
        }
    )


def _oracle_admin_async_config(oracle_service: OracleService) -> OracleAsyncConfig:
    return OracleAsyncConfig(
        connection_config={
            "host": oracle_service.host,
            "port": oracle_service.port,
            "service_name": oracle_service.service_name,
            "user": "system",
            "password": oracle_service.system_password,
            "min": 1,
            "max": 5,
        }
    )


def _create_oracle_schema(oracle_service: OracleService, schema: str) -> None:
    password = f"{schema}_Pwd1"
    config = _oracle_admin_sync_config(oracle_service)
    try:
        with config.provide_session() as driver:
            driver.execute_script(f'CREATE USER {schema} IDENTIFIED BY "{password}"')
            driver.execute_script(
                f"GRANT CREATE SESSION, CREATE TABLE, CREATE SEQUENCE, UNLIMITED TABLESPACE TO {schema}"
            )
            driver.execute_script(
                "GRANT CREATE ANY TABLE, ALTER ANY TABLE, DROP ANY TABLE, "
                "SELECT ANY TABLE, INSERT ANY TABLE, UPDATE ANY TABLE, DELETE ANY TABLE, "
                "CREATE ANY INDEX, ALTER ANY INDEX, DROP ANY INDEX, "
                f"CREATE ANY SEQUENCE, DROP ANY SEQUENCE, SELECT ANY SEQUENCE TO {oracle_service.user}"
            )
            driver.commit()
    finally:
        if config.connection_instance:
            config.close_pool()


def _drop_oracle_schema(oracle_service: OracleService, schema: str) -> None:
    config = _oracle_admin_sync_config(oracle_service)
    try:
        with config.provide_session() as driver:
            driver.execute_script(
                f"""
                BEGIN
                    EXECUTE IMMEDIATE 'DROP USER {schema} CASCADE';
                EXCEPTION
                    WHEN OTHERS THEN
                        IF SQLCODE != -1918 THEN
                            RAISE;
                        END IF;
                END;
                """
            )
            driver.commit()
    finally:
        if config.connection_instance:
            config.close_pool()


